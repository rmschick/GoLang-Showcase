package main

import "fmt"

func main() {
	var name = "Ryan Schick"
	fmt.Println(name)

	var num1, num2 int = 1, 2
	fmt.Println(num1, num2)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	f := "shorthand for declaring and initializing a variable"
	fmt.Println(f)

	p := 68
	fmt.Println(p)

}
